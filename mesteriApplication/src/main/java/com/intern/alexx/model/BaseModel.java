package com.intern.alexx.model;

public abstract class BaseModel {

	private String id;

	public BaseModel() {
	}

	public BaseModel(String id) {
		this.id = id;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

}
